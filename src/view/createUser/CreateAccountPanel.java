    public void colorTextField(String textField) {
        switch (textField) {
            case "name":
                this.nameTextField.setBackground(Color.red);
                break;
            case "surname":
                this.surnameTextField.setBackground(Color.red);
                break;
            case "email":
                this.emailUserNameTextField.setBackground(Color.red);
                this.emailDomainTextField.setBackground(Color.red);
                this.emailDotComTextField.setBackground(Color.red);
                break;
            case "username":
                this.userNameTextField.setBackground(Color.red);
                break;
            case "password":
                this.passwordTextField.setBackground(Color.red);
                break;
            default:
                break;
        }
    }

    public boolean checkEmailFields() {
        if (emailUserNameTextField.getText().isBlank() || emailDomainTextField.getText().isBlank() || emailDotComTextField.getText().isBlank()) {
            colorTextField("email");
            return false;
        }
        return true;
    }

