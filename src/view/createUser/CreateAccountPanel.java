    private void generateUserNameBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_generateUserNameBtnActionPerformed
        if (generateUserNameListener != null) {
            String name = nameTextField.getText();
            String surname = surnameTextField.getText();
            String email = emailUserNameTextField.getText();

            GenerateUsernameEvent ev = new GenerateUsernameEvent(name, surname, email, this);
            generateUserNameListener.generateUsernameEventOccured(ev);
        }
    }//GEN-LAST:event_generateUserNameBtnActionPerformed

